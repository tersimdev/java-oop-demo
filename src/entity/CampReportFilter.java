package entity;

/**
 * <p>
 * This is an enum to represent what the filters used on camp report generation
 * </p>
 * 
 * @author Lim Jun Rong Ryan
 * @version 1.0
 * @since 19-11-2023
 */
public enum CampReportFilter {
    /**
     * No filter
     */
    NONE,
    /**
     * Filter attendees
     */
    ATTENDEE,
    /**
     * Filter camp committee members
     */
    CAMP_COMMITTEE
}
